# List available figlet fonts
list_clock_fonts() {
    echo
    echo -e "${BOLD}Available Clock Fonts${NC}"
    echo "─────────────────────"

    if command -v figlet &>/dev/null; then
        local figlet_dir
        figlet_dir=$(figlet -I2 2>/dev/null)
        if [[ -d "$figlet_dir" ]]; then
            echo "figlet fonts in $figlet_dir:"
            # shellcheck disable=SC2012,SC2011
            ls "$figlet_dir"/*.flf 2>/dev/null | xargs -n1 basename 2>/dev/null | sed 's/\.flf$//' | column
        fi
    else
        echo -e "${YELLOW}figlet not installed${NC}"
        echo "Install with: sudo apt install figlet"
    fi

    if command -v toilet &>/dev/null; then
        local toilet_dir="/usr/share/figlet"
        if [[ -d "$toilet_dir" ]]; then
            echo
            echo "toilet fonts in $toilet_dir:"
            # shellcheck disable=SC2012,SC2011
            ls "$toilet_dir"/*.tlf 2>/dev/null | xargs -n1 basename 2>/dev/null | sed 's/\.tlf$//' | column
        fi
    fi
    echo
}

# Get all available TTE effects (cached)
get_all_effects() {
    # Return cached result if available
    if [[ -n "$EFFECTS_CACHE" ]]; then
        echo "$EFFECTS_CACHE"
        return
    fi

    # Check if tte is available
    if ! command -v tte &>/dev/null; then
        log_error "tte (TerminalTextEffects) not installed"
        return 1
    fi

    # Parse effects from TTE help output
    # Format: {effect1,effect2,...}
    EFFECTS_CACHE=$(tte -h 2>&1 | grep -oE '\{beams,[^}]+\}' | head -1 | tr -d '{}' | tr ',' '\n' | sort)
    echo "$EFFECTS_CACHE"
}

# Validate that effect names exist
# Returns 0 if all effects are valid, 1 otherwise
validate_effects() {
    local effects_to_check="$1"
    local all_effects

    all_effects=$(get_all_effects)
    if [[ -z "$all_effects" ]]; then
        return 1
    fi

    local invalid_effects=""
    IFS=',' read -ra effect_arr <<< "$effects_to_check"
    for effect in "${effect_arr[@]}"; do
        # Trim whitespace
        effect=$(echo "$effect" | tr -d ' ')
        if [[ -z "$effect" ]]; then
            continue
        fi
        if ! echo "$all_effects" | grep -qx "$effect"; then
            if [[ -z "$invalid_effects" ]]; then
                invalid_effects="$effect"
            else
                invalid_effects="$invalid_effects, $effect"
            fi
        fi
    done

    if [[ -n "$invalid_effects" ]]; then
        log_error "Invalid effect(s): $invalid_effects"
        log_info "Use '$0 effects list' to see available effects"
        return 1
    fi
    return 0
}

# List effects with status
list_effects() {
    load_config
    local all_effects
    all_effects=$(get_all_effects)

    echo
    echo -e "${BOLD}Available Effects${NC}"
    echo "─────────────────────"

    if [[ -n "$INCLUDE_EFFECTS" ]]; then
        echo -e "Mode: ${GREEN}Include list active${NC}"
        echo
        local include_arr
        IFS=',' read -ra include_arr <<< "$INCLUDE_EFFECTS"
        for effect in $all_effects; do
            local marker=" "
            for inc in "${include_arr[@]}"; do
                if [[ "$effect" == "$inc" ]]; then
                    marker="${GREEN}✓${NC}"
                    break
                fi
            done
            echo -e "  $marker $effect"
        done
    else
        echo -e "Mode: ${YELLOW}Exclude list active${NC}"
        echo
        local exclude_arr
        IFS=',' read -ra exclude_arr <<< "$EXCLUDE_EFFECTS"
        for effect in $all_effects; do
            local marker="${GREEN}✓${NC}"
            for exc in "${exclude_arr[@]}"; do
                if [[ "$effect" == "$exc" ]]; then
                    marker="${RED}✗${NC}"
                    break
                fi
            done
            echo -e "  $marker $effect"
        done
    fi
    echo
}

# Set include effects list
set_include_effects() {
    local effects="$1"

    load_config

    # Validate effects if not empty
    if [[ -n "$effects" ]]; then
        if ! validate_effects "$effects"; then
            return 1
        fi
    fi

    INCLUDE_EFFECTS="$effects"
    save_config

    if [[ -n "$effects" ]]; then
        log_info "Include effects set to: $effects"
        log_info "Exclude effects will be ignored"
    else
        log_info "Include effects cleared (using exclude list)"
    fi
}

# Set exclude effects list
set_exclude_effects() {
    local effects="$1"

    load_config

    # Validate effects if not empty
    if [[ -n "$effects" ]]; then
        if ! validate_effects "$effects"; then
            return 1
        fi
    fi

    EXCLUDE_EFFECTS="$effects"
    save_config

    log_info "Exclude effects set to: $effects"
    if [[ -n "$INCLUDE_EFFECTS" ]]; then
        log_warn "Note: Include effects is set, exclude will be ignored"
    fi
}

# Add effects to include list
add_include_effects() {
    local new_effects="$1"

    load_config

    # Validate effects before adding
    if ! validate_effects "$new_effects"; then
        return 1
    fi

    if [[ -z "$INCLUDE_EFFECTS" ]]; then
        INCLUDE_EFFECTS="$new_effects"
    else
        INCLUDE_EFFECTS="${INCLUDE_EFFECTS},${new_effects}"
    fi
    save_config

    log_info "Added to include list: $new_effects"
}

# Add effects to exclude list
add_exclude_effects() {
    local new_effects="$1"

    load_config

    # Validate effects before adding
    if ! validate_effects "$new_effects"; then
        return 1
    fi

    if [[ -z "$EXCLUDE_EFFECTS" ]]; then
        EXCLUDE_EFFECTS="$new_effects"
    else
        EXCLUDE_EFFECTS="${EXCLUDE_EFFECTS},${new_effects}"
    fi
    save_config

    log_info "Added to exclude list: $new_effects"
}

# Clear effects lists
clear_effects() {
    local list="${1:-both}"

    load_config

    case "$list" in
        include)
            INCLUDE_EFFECTS=""
            log_info "Include effects cleared"
            ;;
        exclude)
            EXCLUDE_EFFECTS=""
            log_info "Exclude effects cleared"
            ;;
        both|all)
            INCLUDE_EFFECTS=""
            EXCLUDE_EFFECTS=""
            log_info "All effects lists cleared"
            ;;
    esac

    save_config
}

# List available logos
list_logos() {
    local logos_dir="${SCRIPT_DIR}/logos"
    echo
    echo -e "${BOLD}Available Logos${NC}"
    echo "────────────────"

    if [[ -d "$logos_dir" ]]; then
        local i=1
        for logo in "$logos_dir"/*.txt; do
            if [[ -f "$logo" ]]; then
                local name
                name=$(basename "$logo" .txt)
                local current=""
                if [[ "$(readlink -f "$LOGO_FILE" 2>/dev/null)" == "$(readlink -f "$logo")" ]] || \
                   diff -q "$LOGO_FILE" "$logo" &>/dev/null 2>&1; then
                    current=" ${GREEN}(current)${NC}"
                fi
                echo -e "  $i) $name$current"
                ((i++))
            fi
        done
    else
        log_warn "Logos directory not found: $logos_dir"
    fi
    echo
}

# Set logo
set_logo() {
    local selection="$1"
    local logos_dir="${SCRIPT_DIR}/logos"

    # Check if selection is a number or a name
    if [[ "$selection" =~ ^[0-9]+$ ]]; then
        # Selection by number
        local logos=("$logos_dir"/*.txt)
        local index=$((selection - 1))
        if [[ $index -ge 0 ]] && [[ $index -lt ${#logos[@]} ]]; then
            local logo_path="${logos[$index]}"
            cp "$logo_path" "$SCREENSAVER_DIR/logo.txt"
            LOGO_FILE="$SCREENSAVER_DIR/logo.txt"
            save_config
            log_success "Logo set to: $(basename "$logo_path" .txt)"
        else
            log_error "Invalid selection: $selection"
            return 1
        fi
    else
        # Selection by name
        local logo_path="$logos_dir/${selection}.txt"
        if [[ -f "$logo_path" ]]; then
            cp "$logo_path" "$SCREENSAVER_DIR/logo.txt"
            LOGO_FILE="$SCREENSAVER_DIR/logo.txt"
            save_config
            log_success "Logo set to: $selection"
        else
            log_error "Logo not found: $selection"
            log_info "Available logos:"
            list_logos
            return 1
        fi
    fi
}

# Preview a logo
preview_logo() {
    local selection="$1"
    local logos_dir="${SCRIPT_DIR}/logos"
    local logo_path

    if [[ "$selection" =~ ^[0-9]+$ ]]; then
        local logos=("$logos_dir"/*.txt)
        local index=$((selection - 1))
        logo_path="${logos[$index]}"
    else
        logo_path="$logos_dir/${selection}.txt"
    fi

    if [[ -f "$logo_path" ]]; then
        echo
        echo -e "${BOLD}Preview: $(basename "$logo_path" .txt)${NC}"
        echo "────────────────────────────────────"
        cat "$logo_path"
        echo "────────────────────────────────────"
    else
        log_error "Logo not found"
    fi
}

# Interactive configuration menu
interactive_menu() {
    load_config

    while true; do
        echo
        echo -e "${BOLD}COSMIC Screensaver Configuration${NC}"
        echo "─────────────────────────────────────"
        echo "1) Show status"
        echo "2) Enable screensaver"
        echo "3) Disable screensaver"
        echo "4) Set idle timeout"
        echo "5) Set lock timeout"
        echo "6) Set screen off timeout"
        echo "7) Configure power settings"
        echo "8) Change logo"
        echo "9) Change terminal"
        echo "t) Test screensaver"
        echo "i) Install dependencies"
        echo "0) Exit"
        echo
        read -r -p "Select option: " choice

        case "$choice" in
            1) show_status ;;
            2) enable_service ;;
            3) disable_service ;;
            4)
                read -r -p "Idle timeout (seconds) [${IDLE_TIMEOUT}]: " value
                [[ -n "$value" ]] && set_timer idle "$value"
                ;;
            5)
                read -r -p "Lock timeout (seconds, 0 to disable) [${LOCK_TIMEOUT}]: " value
                [[ -n "$value" ]] && set_timer lock "$value"
                ;;
            6)
                read -r -p "Screen off timeout (seconds, 0 to disable) [${DPMS_TIMEOUT}]: " value
                [[ -n "$value" ]] && set_timer dpms "$value"
                ;;
            7)
                echo
                echo "Power Settings:"
                echo "1) Disable screensaver on battery: $DISABLE_ON_BATTERY"
                echo "2) Battery idle timeout: ${BATTERY_IDLE_TIMEOUT}s"
                read -r -p "Select option: " power_choice
                case "$power_choice" in
                    1)
                        if [[ "$DISABLE_ON_BATTERY" == "true" ]]; then
                            set_power_option battery-disable false
                        else
                            set_power_option battery-disable true
                        fi
                        ;;
                    2)
                        read -r -p "Battery idle timeout (seconds): " value
                        [[ -n "$value" ]] && set_timer battery "$value"
                        ;;
                esac
                ;;
            8)
                list_logos
                read -r -p "Select logo (number or name), or 'p' to preview: " logo_choice
                if [[ "$logo_choice" == "p" ]]; then
                    read -r -p "Logo to preview: " preview_choice
                    preview_logo "$preview_choice"
                elif [[ -n "$logo_choice" ]]; then
                    set_logo "$logo_choice"
                fi
                ;;
            9)
                echo
                echo "Terminal Emulators:"
                echo "1) ghostty $(if [[ "$TERMINAL" == "ghostty" ]]; then echo "(current)"; fi)"
                echo "2) cosmic-term $(if [[ "$TERMINAL" == "cosmic-term" ]]; then echo "(current)"; fi)"
                read -r -p "Select terminal: " term_choice
                case "$term_choice" in
                    1) set_terminal ghostty ;;
                    2) set_terminal cosmic-term ;;
                    *) log_error "Invalid option" ;;
                esac
                ;;
            t|T)
                log_info "Starting screensaver test (press any key to exit)..."
                "$SCREENSAVER_SCRIPT" test
                ;;
            i|I)
                install_dependencies
                ;;
            0) exit 0 ;;
            *) log_error "Invalid option" ;;
        esac
    done
}

# Install dependencies
install_dependencies() {
    echo
    log_info "Installing dependencies..."

    # Check for Ghostty (required)
    if ! command -v ghostty &>/dev/null; then
        log_error "Ghostty is required but not installed"
        log_info "Install Ghostty from: https://ghostty.org"
        log_info "  - Flatpak: flatpak install flathub com.mitchellh.ghostty"
        log_info "  - Or build from source: https://github.com/ghostty-org/ghostty"
        return 1
    fi
    log_success "Ghostty is installed"

    # Check for pipx/pip
    if command -v pipx &>/dev/null; then
        log_info "Installing TerminalTextEffects with pipx..."
        pipx install terminaltexteffects
    elif command -v pip &>/dev/null; then
        log_info "Installing TerminalTextEffects with pip..."
        pip install --user terminaltexteffects
    else
        log_error "Neither pipx nor pip found. Install Python first."
        return 1
    fi

    # Check for swayidle
    if ! command -v swayidle &>/dev/null; then
        log_info "Installing swayidle..."
        if command -v apt &>/dev/null; then
            sudo apt install -y swayidle
        else
            log_warn "Please install swayidle manually for your distribution"
        fi
    fi

    # Check for wtype (optional, for auto-fullscreen on COSMIC)
    if ! command -v wtype &>/dev/null; then
        log_info "Installing wtype (for auto-fullscreen support)..."
        if command -v apt &>/dev/null; then
            sudo apt install -y wtype
        else
            log_warn "Please install wtype manually for auto-fullscreen support"
        fi
    fi

    log_success "Dependencies installed"
}

# Initial setup
setup() {
    ensure_dirs

    # Copy default logo if none exists
    if [[ ! -f "$SCREENSAVER_DIR/logo.txt" ]]; then
        local default_logo="${SCRIPT_DIR}/logos/pop-os-name-with-icon.txt"
        if [[ -f "$default_logo" ]]; then
            cp "$default_logo" "$SCREENSAVER_DIR/logo.txt"
            log_info "Copied default logo (pop-os-name-with-icon)"
        else
            # Fallback: use first available logo
            local first_logo
            first_logo=$(find "${SCRIPT_DIR}/logos" -name "*.txt" -type f | head -1)
            if [[ -n "$first_logo" ]]; then
                cp "$first_logo" "$SCREENSAVER_DIR/logo.txt"
                log_info "Copied default logo ($(basename "$first_logo" .txt))"
            fi
        fi
    fi

    # Symlink launcher script
    if [[ ! -f "$SCREENSAVER_DIR/run-screensaver.sh" ]] && [[ -f "${SCRIPT_DIR}/run-screensaver.sh" ]]; then
        ln -sf "${SCRIPT_DIR}/run-screensaver.sh" "$SCREENSAVER_DIR/run-screensaver.sh"
        log_info "Linked launcher script"
    fi

    # Symlink main screensaver script
    if [[ ! -f "$SCREENSAVER_DIR/cosmic-screensaver.sh" ]]; then
        ln -sf "${SCRIPT_DIR}/cosmic-screensaver.sh" "$SCREENSAVER_DIR/cosmic-screensaver.sh"
        log_info "Linked screensaver script"
    fi

    # Symlink screensaver-ctl to ~/.local/bin for PATH access
    local local_bin="$HOME/.local/bin"
    mkdir -p "$local_bin"
    if [[ ! -L "$local_bin/screensaver-ctl" ]] || [[ "$(readlink -f "$local_bin/screensaver-ctl")" != "$(readlink -f "${SCRIPT_DIR}/screensaver-ctl.sh")" ]]; then
        ln -sf "${SCRIPT_DIR}/screensaver-ctl.sh" "$local_bin/screensaver-ctl"
        log_info "Linked screensaver-ctl to ~/.local/bin"
    fi

    load_config
    save_config
    log_success "Initial setup complete"
    echo
    log_info "Next steps:"
    echo "  1. Run: $0 install  - to install dependencies"
    echo "  2. Run: $0 enable   - to enable the screensaver"
    echo "  3. Run: $0 test     - to test the screensaver"
    echo
    log_info "You can now run 'screensaver-ctl' from anywhere (if ~/.local/bin is in PATH)"
}

# Add COSMIC keyboard shortcut
add_shortcut() {
    local system_actions="$HOME/.config/cosmic/com.system76.CosmicSettings.Shortcuts/v1/system_actions"
    local defaults="$HOME/.config/cosmic/com.system76.CosmicSettings.Shortcuts/v1/defaults"
    local launcher_path="${FULLSCREEN_LAUNCHER}"
    local escaped_path="${launcher_path//\//\\/}"

    if [[ ! -f "$system_actions" ]] || [[ ! -f "$defaults" ]]; then
        log_error "COSMIC shortcuts config not found"
        log_info "Please configure COSMIC shortcuts manually"
        return 1
    fi

    # Check if screensaver already exists and update if needed
    if grep -q "Screensaver:" "$system_actions" 2>/dev/null; then
        # Update existing shortcut to use fullscreen launcher
        if grep -q "cosmic-term" "$system_actions" 2>/dev/null; then
            cp "$system_actions" "${system_actions}.backup"
            sed -i "s|Screensaver: \"cosmic-term.*\"|Screensaver: \"${launcher_path} launch\"|" "$system_actions"
            log_success "Updated Screensaver to use fullscreen launcher"
        else
            log_info "Screensaver shortcut already configured for fullscreen"
        fi
    else
        # Backup original
        cp "$system_actions" "${system_actions}.backup"

        # Add screensaver action before closing brace
        sed -i "s|}$|    /// Launches the terminal screensaver (fullscreen)\n    Screensaver: \"${escaped_path} launch\",\n}|" "$system_actions"

        log_success "Added Screensaver to system_actions"
    fi

    # Check if keybinding already exists
    if grep -q "System(Screensaver)" "$defaults" 2>/dev/null; then
        log_warn "Screensaver keybinding already exists"
    else
        # Backup original
        cp "$defaults" "${defaults}.backup"

        # Add keybinding before closing brace
        sed -i 's/}$/\n    \/\/ Screensaver: Super+Shift+Z\n    (modifiers: [Super, Shift], key: "z"): System(Screensaver),\n}/' "$defaults"

        log_success "Added Super+Shift+Z keybinding"
    fi

    echo
    log_info "Changes applied. Please log out and back in for shortcuts to take effect."
    log_info "Backups saved as *.backup files"
}

