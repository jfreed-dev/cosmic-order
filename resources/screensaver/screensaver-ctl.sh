#!/bin/bash
# screensaver-ctl.sh - Management script for COSMIC screensaver
# Handles configuration, swayidle integration, and power settings

set -uo pipefail

# Paths
SCREENSAVER_DIR="${SCREENSAVER_DIR:-$HOME/.config/cosmic-screensaver}"
CONFIG_FILE="${SCREENSAVER_DIR}/config"
SWAYIDLE_CONFIG="${SCREENSAVER_DIR}/swayidle.conf"
SYSTEMD_USER_DIR="$HOME/.config/systemd/user"
SCRIPT_DIR="$(dirname "$(readlink -f "$0")")"
SCREENSAVER_SCRIPT="${SCRIPT_DIR}/cosmic-screensaver.sh"
FULLSCREEN_LAUNCHER="${SCRIPT_DIR}/launch-fullscreen.sh"

# Default settings
DEFAULT_IDLE_TIMEOUT=300      # 5 minutes
DEFAULT_LOCK_TIMEOUT=600      # 10 minutes (after screensaver)
DEFAULT_DPMS_TIMEOUT=900      # 15 minutes (screen off)
DEFAULT_FRAME_RATE=60
DEFAULT_INCLUDE_EFFECTS=""    # empty = use all (or exclude list)
DEFAULT_EXCLUDE_EFFECTS="dev_worm"
DEFAULT_FADE_IN_EFFECT=""     # e.g., expand, slide, middleout
DEFAULT_FADE_OUT_EFFECT=""    # e.g., burn, crumble, scattered
DEFAULT_SHOW_CLOCK="false"    # Show time between effects
DEFAULT_CLOCK_DURATION=3      # Seconds to display clock
DEFAULT_CLOCK_FORMAT="%H:%M"  # Time format
DEFAULT_CLOCK_FONT=""         # figlet/toilet font
DEFAULT_TERMINAL="ghostty"    # ghostty or cosmic-term
DEFAULT_LOCK_COMMAND="cosmic-greeter --lock"  # Lock screen command
DEFAULT_CURSOR_HIDE="true"    # Hide text cursor during screensaver
DEFAULT_HIDE_MOUSE="true"     # Hide mouse pointer via terminal config
DEFAULT_DISMISS_ON_KEY="true" # Keyboard input dismisses screensaver

# Cache for effect list (populated on first call)
EFFECTS_CACHE=""

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m'

log_info() { echo -e "${CYAN}[INFO]${NC} $*"; }
log_success() { echo -e "${GREEN}[OK]${NC} $*"; }
log_warn() { echo -e "${YELLOW}[WARN]${NC} $*"; }
log_error() { echo -e "${RED}[ERROR]${NC} $*" >&2; }

# Ensure directories exist
ensure_dirs() {
    mkdir -p "$SCREENSAVER_DIR"
    mkdir -p "$SYSTEMD_USER_DIR"
}

# Load current configuration
load_config() {
    # Set defaults
    ENABLED="${ENABLED:-true}"
    IDLE_TIMEOUT="${IDLE_TIMEOUT:-$DEFAULT_IDLE_TIMEOUT}"
    LOCK_TIMEOUT="${LOCK_TIMEOUT:-$DEFAULT_LOCK_TIMEOUT}"
    DPMS_TIMEOUT="${DPMS_TIMEOUT:-$DEFAULT_DPMS_TIMEOUT}"
    FRAME_RATE="${FRAME_RATE:-$DEFAULT_FRAME_RATE}"
    INCLUDE_EFFECTS="${INCLUDE_EFFECTS:-$DEFAULT_INCLUDE_EFFECTS}"
    EXCLUDE_EFFECTS="${EXCLUDE_EFFECTS:-$DEFAULT_EXCLUDE_EFFECTS}"
    FADE_IN_EFFECT="${FADE_IN_EFFECT:-$DEFAULT_FADE_IN_EFFECT}"
    FADE_OUT_EFFECT="${FADE_OUT_EFFECT:-$DEFAULT_FADE_OUT_EFFECT}"
    SHOW_CLOCK="${SHOW_CLOCK:-$DEFAULT_SHOW_CLOCK}"
    CLOCK_DURATION="${CLOCK_DURATION:-$DEFAULT_CLOCK_DURATION}"
    CLOCK_FORMAT="${CLOCK_FORMAT:-$DEFAULT_CLOCK_FORMAT}"
    CLOCK_FONT="${CLOCK_FONT:-$DEFAULT_CLOCK_FONT}"
    LOGO_FILE="${LOGO_FILE:-$SCREENSAVER_DIR/logo.txt}"
    DISABLE_ON_BATTERY="${DISABLE_ON_BATTERY:-false}"
    BATTERY_IDLE_TIMEOUT="${BATTERY_IDLE_TIMEOUT:-600}"
    TERMINAL="${TERMINAL:-$DEFAULT_TERMINAL}"
    LOCK_COMMAND="${LOCK_COMMAND:-$DEFAULT_LOCK_COMMAND}"
    CURSOR_HIDE="${CURSOR_HIDE:-$DEFAULT_CURSOR_HIDE}"
    HIDE_MOUSE="${HIDE_MOUSE:-$DEFAULT_HIDE_MOUSE}"
    DISMISS_ON_KEY="${DISMISS_ON_KEY:-$DEFAULT_DISMISS_ON_KEY}"

    if [[ -f "$CONFIG_FILE" ]]; then
        # shellcheck source=/dev/null
        source "$CONFIG_FILE"
    fi
}

# Save configuration
save_config() {
    cat > "$CONFIG_FILE" << EOF
# COSMIC Screensaver Configuration
# Generated by screensaver-ctl.sh

# Enable/disable screensaver
ENABLED="${ENABLED}"

# Idle timeout before screensaver starts (seconds)
IDLE_TIMEOUT="${IDLE_TIMEOUT}"

# Lock timeout after screensaver (seconds, 0 to disable)
LOCK_TIMEOUT="${LOCK_TIMEOUT}"

# DPMS (screen off) timeout (seconds, 0 to disable)
DPMS_TIMEOUT="${DPMS_TIMEOUT}"

# Animation frame rate (lower = less CPU)
FRAME_RATE="${FRAME_RATE}"

# Effects to include (comma-separated, takes precedence over exclude)
INCLUDE_EFFECTS="${INCLUDE_EFFECTS}"

# Effects to exclude (comma-separated, ignored if include is set)
EXCLUDE_EFFECTS="${EXCLUDE_EFFECTS}"

# Fade transition effects (empty to disable)
FADE_IN_EFFECT="${FADE_IN_EFFECT}"
FADE_OUT_EFFECT="${FADE_OUT_EFFECT}"

# Clock display between effects
SHOW_CLOCK="${SHOW_CLOCK}"
CLOCK_DURATION="${CLOCK_DURATION}"
CLOCK_FORMAT="${CLOCK_FORMAT}"
CLOCK_FONT="${CLOCK_FONT}"

# Logo file path
LOGO_FILE="${LOGO_FILE}"

# Power settings
DISABLE_ON_BATTERY="${DISABLE_ON_BATTERY}"
BATTERY_IDLE_TIMEOUT="${BATTERY_IDLE_TIMEOUT}"

# Terminal emulator (ghostty or cosmic-term)
TERMINAL="${TERMINAL}"

# Lock screen command (customize for different lock screens)
LOCK_COMMAND="${LOCK_COMMAND}"

# Cursor & input handling
CURSOR_HIDE="${CURSOR_HIDE}"
HIDE_MOUSE="${HIDE_MOUSE}"
DISMISS_ON_KEY="${DISMISS_ON_KEY}"
EOF
    log_success "Configuration saved to $CONFIG_FILE"
}

# Check power status
is_on_battery() {
    local status
    status=$(cat /sys/class/power_supply/BAT*/status 2>/dev/null | head -1)
    [[ "$status" == "Discharging" ]]
}

# Get effective idle timeout based on power status
get_effective_timeout() {
    if [[ "$DISABLE_ON_BATTERY" == "true" ]] && is_on_battery; then
        echo "$BATTERY_IDLE_TIMEOUT"
    else
        echo "$IDLE_TIMEOUT"
    fi
}

# Generate swayidle configuration
generate_swayidle_config() {
    local timeout
    timeout=$(get_effective_timeout)

    # Use fullscreen launcher
    local launch_cmd="${FULLSCREEN_LAUNCHER} launch"
    local kill_cmd="${FULLSCREEN_LAUNCHER} kill"

    cat > "$SWAYIDLE_CONFIG" << EOF
# Swayidle configuration for COSMIC Screensaver
# Generated by screensaver-ctl.sh
# Reload: systemctl --user restart cosmic-screensaver-idle.service

timeout ${timeout} '${launch_cmd}' resume '${kill_cmd}'
EOF

    # Add lock timeout if set
    if [[ "$LOCK_TIMEOUT" -gt 0 ]]; then
        local lock_time=$((timeout + LOCK_TIMEOUT))
        echo "timeout ${lock_time} '${LOCK_COMMAND}'" >> "$SWAYIDLE_CONFIG"
    fi

    # Add DPMS timeout if set (resume must be on same line as timeout)
    if [[ "$DPMS_TIMEOUT" -gt 0 ]]; then
        echo "timeout ${DPMS_TIMEOUT} 'cosmic-randr output \"*\" --off' resume 'cosmic-randr output \"*\" --on'" >> "$SWAYIDLE_CONFIG"
    fi

    # Handle before-sleep (system suspend)
    cat >> "$SWAYIDLE_CONFIG" << EOF
before-sleep '${LOCK_COMMAND}'
EOF

    log_success "Generated swayidle config: $SWAYIDLE_CONFIG"
}

# Create systemd user service for swayidle
create_systemd_service() {
    local service_file="${SYSTEMD_USER_DIR}/cosmic-screensaver-idle.service"

    cat > "$service_file" << EOF
[Unit]
Description=COSMIC Screensaver Idle Daemon
Documentation=https://github.com/swaywm/swayidle
PartOf=graphical-session.target
After=graphical-session.target

[Service]
Type=simple
ExecStart=/usr/bin/swayidle -w -C ${SWAYIDLE_CONFIG}
ExecReload=/bin/kill -HUP \$MAINPID
Restart=on-failure
RestartSec=5

[Install]
WantedBy=graphical-session.target
EOF

    log_success "Created systemd service: $service_file"
}

# Install swayidle if not present
check_swayidle() {
    if ! command -v swayidle &>/dev/null; then
        log_warn "swayidle is not installed."
        log_info "Install with: sudo apt install swayidle"
        return 1
    fi
    return 0
}

# Enable screensaver service
enable_service() {
    if ! check_swayidle; then
        return 1
    fi

    ensure_dirs
    load_config
    ENABLED="true"
    save_config
    generate_swayidle_config
    create_systemd_service

    systemctl --user daemon-reload
    systemctl --user enable cosmic-screensaver-idle.service
    systemctl --user start cosmic-screensaver-idle.service

    log_success "Screensaver enabled and started"
}

# Disable screensaver service
disable_service() {
    load_config
    ENABLED="false"
    save_config

    systemctl --user stop cosmic-screensaver-idle.service 2>/dev/null || true
    systemctl --user disable cosmic-screensaver-idle.service 2>/dev/null || true

    log_success "Screensaver disabled"
}

# Reload configuration
reload_service() {
    load_config
    generate_swayidle_config
    systemctl --user restart cosmic-screensaver-idle.service 2>/dev/null || true
    log_success "Configuration reloaded"
}

# Show current status
show_status() {
    load_config
    echo
    echo -e "${BOLD}COSMIC Screensaver Status${NC}"
    echo "─────────────────────────────────"

    # Service status
    local service_status
    if systemctl --user is-active cosmic-screensaver-idle.service &>/dev/null; then
        service_status="${GREEN}running${NC}"
    else
        service_status="${RED}stopped${NC}"
    fi
    echo -e "Service:          $service_status"
    echo -e "Enabled:          ${ENABLED}"
    echo

    # Timers
    echo -e "${BOLD}Timeouts${NC}"
    echo -e "Idle timeout:     ${IDLE_TIMEOUT}s ($(( IDLE_TIMEOUT / 60 )) min)"
    echo -e "Lock timeout:     ${LOCK_TIMEOUT}s ($(( LOCK_TIMEOUT / 60 )) min after idle)"
    echo -e "Screen off:       ${DPMS_TIMEOUT}s ($(( DPMS_TIMEOUT / 60 )) min)"
    echo -e "Lock command:     ${LOCK_COMMAND}"
    echo

    # Power settings
    echo -e "${BOLD}Power Settings${NC}"
    echo -e "Disable on battery: ${DISABLE_ON_BATTERY}"
    if [[ "$DISABLE_ON_BATTERY" == "true" ]]; then
        echo -e "Battery timeout:    ${BATTERY_IDLE_TIMEOUT}s"
    fi

    # Current power status
    if is_on_battery; then
        echo -e "Current power:      ${YELLOW}Battery${NC}"
    else
        echo -e "Current power:      ${GREEN}AC${NC}"
    fi
    echo

    # Animation settings
    echo -e "${BOLD}Animation${NC}"
    echo -e "Frame rate:       ${FRAME_RATE} FPS"
    if [[ -n "$INCLUDE_EFFECTS" ]]; then
        echo -e "Include effects:  ${GREEN}${INCLUDE_EFFECTS}${NC}"
        echo -e "Exclude effects:  ${YELLOW}(ignored - include is set)${NC}"
    else
        echo -e "Include effects:  ${YELLOW}(all)${NC}"
        echo -e "Exclude effects:  ${EXCLUDE_EFFECTS}"
    fi
    echo -e "Logo file:        ${LOGO_FILE}"
    echo -e "Terminal:         ${TERMINAL}"
    if [[ -n "$FADE_IN_EFFECT" ]]; then
        echo -e "Fade in:          ${GREEN}${FADE_IN_EFFECT}${NC}"
    else
        echo -e "Fade in:          ${YELLOW}(disabled)${NC}"
    fi
    if [[ -n "$FADE_OUT_EFFECT" ]]; then
        echo -e "Fade out:         ${GREEN}${FADE_OUT_EFFECT}${NC}"
    else
        echo -e "Fade out:         ${YELLOW}(disabled)${NC}"
    fi
    if [[ "$SHOW_CLOCK" == "true" ]]; then
        echo -e "Clock:            ${GREEN}enabled${NC} (${CLOCK_DURATION}s, format: ${CLOCK_FORMAT})"
        if [[ -n "$CLOCK_FONT" ]]; then
            echo -e "Clock font:       ${CLOCK_FONT}"
        fi
    else
        echo -e "Clock:            ${YELLOW}(disabled)${NC}"
    fi
    echo

    # Cursor & Input settings
    echo -e "${BOLD}Cursor & Input${NC}"
    if [[ "$CURSOR_HIDE" == "true" ]]; then
        echo -e "Hide cursor:      ${GREEN}yes${NC}"
    else
        echo -e "Hide cursor:      ${YELLOW}no${NC}"
    fi
    if [[ "$HIDE_MOUSE" == "true" ]]; then
        echo -e "Hide mouse:       ${GREEN}yes${NC}"
    else
        echo -e "Hide mouse:       ${YELLOW}no${NC}"
    fi
    if [[ "$DISMISS_ON_KEY" == "true" ]]; then
        echo -e "Dismiss on key:   ${GREEN}yes${NC}"
    else
        echo -e "Dismiss on key:   ${YELLOW}no${NC}"
    fi

    # Dependency check
    echo -e "${BOLD}Dependencies${NC}"
    if [[ "$TERMINAL" == "ghostty" ]]; then
        if command -v ghostty &>/dev/null; then
            echo -e "ghostty:          ${GREEN}installed${NC} (selected)"
        else
            echo -e "ghostty:          ${RED}not installed${NC} (selected but missing!)"
        fi
        if command -v cosmic-term &>/dev/null; then
            echo -e "cosmic-term:      ${GREEN}installed${NC} (available)"
        else
            echo -e "cosmic-term:      ${YELLOW}not installed${NC} (optional)"
        fi
    else
        if command -v cosmic-term &>/dev/null; then
            echo -e "cosmic-term:      ${GREEN}installed${NC} (selected)"
        else
            echo -e "cosmic-term:      ${RED}not installed${NC} (selected but missing!)"
        fi
        if command -v ghostty &>/dev/null; then
            echo -e "ghostty:          ${GREEN}installed${NC} (available)"
        else
            echo -e "ghostty:          ${YELLOW}not installed${NC} (optional)"
        fi
    fi
    if command -v tte &>/dev/null; then
        echo -e "tte:              ${GREEN}installed${NC} (required)"
    else
        echo -e "tte:              ${RED}not installed${NC} (required)"
    fi
    if command -v swayidle &>/dev/null; then
        echo -e "swayidle:         ${GREEN}installed${NC}"
    else
        echo -e "swayidle:         ${RED}not installed${NC}"
    fi
    if command -v wtype &>/dev/null; then
        echo -e "wtype:            ${GREEN}installed${NC} (optional, for auto-fullscreen)"
    else
        echo -e "wtype:            ${YELLOW}not installed${NC} (optional, for auto-fullscreen)"
    fi
    echo
}

# Set timer values
set_timer() {
    local timer_type="$1"
    local value="$2"

    load_config

    case "$timer_type" in
        idle)
            IDLE_TIMEOUT="$value"
            log_info "Idle timeout set to ${value}s"
            ;;
        lock)
            LOCK_TIMEOUT="$value"
            log_info "Lock timeout set to ${value}s"
            ;;
        dpms|screen)
            DPMS_TIMEOUT="$value"
            log_info "Screen off timeout set to ${value}s"
            ;;
        battery)
            BATTERY_IDLE_TIMEOUT="$value"
            log_info "Battery idle timeout set to ${value}s"
            ;;
        *)
            log_error "Unknown timer type: $timer_type"
            log_info "Valid types: idle, lock, dpms, battery"
            return 1
            ;;
    esac

    save_config
    reload_service
}

# Set power option
set_power_option() {
    local option="$1"
    local value="$2"

    load_config

    case "$option" in
        battery-disable)
            DISABLE_ON_BATTERY="$value"
            log_info "Disable on battery set to: $value"
            ;;
        *)
            log_error "Unknown power option: $option"
            return 1
            ;;
    esac

    save_config
    reload_service
}

# Set lock command
set_lock_command() {
    local command="$1"

    load_config

    if [[ -z "$command" ]]; then
        log_error "Lock command cannot be empty"
        return 1
    fi

    LOCK_COMMAND="$command"
    save_config
    reload_service

    log_info "Lock command set to: $command"
}

# Set terminal emulator
set_terminal() {
    local terminal="$1"

    load_config

    case "$terminal" in
        ghostty)
            if ! command -v ghostty &>/dev/null; then
                log_warn "Ghostty is not installed"
                log_info "Install Ghostty from: https://ghostty.org"
            fi
            TERMINAL="ghostty"
            log_info "Terminal set to: ghostty"
            ;;
        cosmic-term)
            if ! command -v cosmic-term &>/dev/null; then
                log_warn "cosmic-term is not installed"
                log_info "cosmic-term is included with COSMIC Desktop"
            fi
            TERMINAL="cosmic-term"
            log_info "Terminal set to: cosmic-term"
            ;;
        *)
            log_error "Unknown terminal: $terminal"
            log_info "Valid terminals: ghostty, cosmic-term"
            return 1
            ;;
    esac

    save_config
}

# Set fade-in effect
set_fade_in() {
    local effect="$1"

    load_config

    if [[ -z "$effect" ]] || [[ "$effect" == "none" ]] || [[ "$effect" == "off" ]]; then
        FADE_IN_EFFECT=""
        log_info "Fade-in disabled"
    else
        # Validate effect name
        if ! validate_effects "$effect"; then
            return 1
        fi
        FADE_IN_EFFECT="$effect"
        log_info "Fade-in effect set to: $effect"
    fi

    save_config
}

