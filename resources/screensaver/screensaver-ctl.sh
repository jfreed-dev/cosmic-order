# Main entry point
main() {
    local command="${1:-menu}"
    shift 2>/dev/null || true

    case "$command" in
        status)
            show_status
            ;;
        enable)
            enable_service
            ;;
        disable)
            disable_service
            ;;
        reload)
            reload_service
            ;;
        setup)
            setup
            ;;
        install)
            install_dependencies
            ;;
        test)
            "$SCREENSAVER_SCRIPT" test "$@"
            ;;
        set)
            local option_type="${1:-}"
            local value="${2:-}"
            if [[ -z "$option_type" ]] || [[ -z "$value" ]]; then
                log_error "Usage: $0 set <idle|lock|dpms|battery|terminal|lock-command> VALUE"
                exit 1
            fi
            case "$option_type" in
                terminal)
                    set_terminal "$value"
                    ;;
                lock-command)
                    set_lock_command "$value"
                    ;;
                *)
                    set_timer "$option_type" "$value"
                    ;;
            esac
            ;;
        power)
            local option="${1:-}"
            local value="${2:-}"
            case "$option" in
                battery)
                    if [[ "$value" == "on" ]]; then
                        set_power_option battery-disable true
                    else
                        set_power_option battery-disable false
                    fi
                    ;;
                *)
                    log_error "Usage: $0 power battery <on|off>"
                    exit 1
                    ;;
            esac
            ;;
        menu)
            interactive_menu
            ;;
        effects)
            local subcmd="${1:-list}"
            shift 2>/dev/null || true
            case "$subcmd" in
                list)
                    list_effects
                    ;;
                include)
                    local effects="${1:-}"
                    if [[ -z "$effects" ]]; then
                        log_error "Usage: $0 effects include EFFECT1,EFFECT2,..."
                        exit 1
                    fi
                    set_include_effects "$effects"
                    ;;
                exclude)
                    local effects="${1:-}"
                    if [[ -z "$effects" ]]; then
                        log_error "Usage: $0 effects exclude EFFECT1,EFFECT2,..."
                        exit 1
                    fi
                    set_exclude_effects "$effects"
                    ;;
                add-include)
                    local effects="${1:-}"
                    if [[ -z "$effects" ]]; then
                        log_error "Usage: $0 effects add-include EFFECT1,EFFECT2,..."
                        exit 1
                    fi
                    add_include_effects "$effects"
                    ;;
                add-exclude)
                    local effects="${1:-}"
                    if [[ -z "$effects" ]]; then
                        log_error "Usage: $0 effects add-exclude EFFECT1,EFFECT2,..."
                        exit 1
                    fi
                    add_exclude_effects "$effects"
                    ;;
                clear)
                    local list="${1:-both}"
                    clear_effects "$list"
                    ;;
                *)
                    log_error "Unknown effects subcommand: $subcmd"
                    log_info "Usage: $0 effects <list|include|exclude|add-include|add-exclude|clear>"
                    exit 1
                    ;;
            esac
            ;;
        fade)
            local direction="${1:-}"
            local effect="${2:-}"
            if [[ -z "$direction" ]]; then
                log_error "Usage: $0 fade <in|out> <EFFECT|off>"
                exit 1
            fi
            case "$direction" in
                in)
                    if [[ -z "$effect" ]]; then
                        log_error "Usage: $0 fade in <EFFECT|off>"
                        exit 1
                    fi
                    set_fade_in "$effect"
                    ;;
                out)
                    if [[ -z "$effect" ]]; then
                        log_error "Usage: $0 fade out <EFFECT|off>"
                        exit 1
                    fi
                    set_fade_out "$effect"
                    ;;
                *)
                    log_error "Unknown fade direction: $direction"
                    log_info "Usage: $0 fade <in|out> <EFFECT|off>"
                    exit 1
                    ;;
            esac
            ;;
        clock)
            local subcmd="${1:-}"
            local value="${2:-}"
            if [[ -z "$subcmd" ]]; then
                log_error "Usage: $0 clock <on|off|duration|format|font|fonts>"
                exit 1
            fi
            case "$subcmd" in
                on|off|true|false)
                    set_clock_enabled "$subcmd"
                    ;;
                duration)
                    if [[ -z "$value" ]]; then
                        log_error "Usage: $0 clock duration SECONDS"
                        exit 1
                    fi
                    set_clock_duration "$value"
                    ;;
                format)
                    if [[ -z "$value" ]]; then
                        log_error "Usage: $0 clock format FORMAT"
                        exit 1
                    fi
                    set_clock_format "$value"
                    ;;
                font)
                    set_clock_font "${value:-default}"
                    ;;
                fonts|list-fonts)
                    list_clock_fonts
                    ;;
                *)
                    log_error "Unknown clock subcommand: $subcmd"
                    log_info "Usage: $0 clock <on|off|duration|format|font|fonts>"
                    exit 1
                    ;;
            esac
            ;;
        logos|list-logos)
            load_config
            list_logos
            ;;
        logo)
            load_config
            local selection="${1:-}"
            if [[ -z "$selection" ]]; then
                log_error "Usage: $0 logo <name|number>"
                list_logos
                exit 1
            fi
            set_logo "$selection"
            ;;
        preview)
            load_config
            local selection="${1:-}"
            if [[ -z "$selection" ]]; then
                log_error "Usage: $0 preview <name|number>"
                exit 1
            fi
            preview_logo "$selection"
            ;;
        add-shortcut)
            setup
            add_shortcut
            ;;
        help|-h|--help)
            usage
            ;;
        *)
            log_error "Unknown command: $command"
            usage
            exit 1
            ;;
    esac
}

main "$@"
