}

# Enable/disable clock display
set_clock_enabled() {
    local enabled="$1"

    load_config

    case "$enabled" in
        on|true|yes|1)
            SHOW_CLOCK="true"
            log_info "Clock display enabled"
            # Check for figlet/toilet
            if ! command -v figlet &>/dev/null && ! command -v toilet &>/dev/null; then
                log_warn "figlet/toilet not installed - clock will use plain text"
                log_info "Install with: sudo apt install figlet"
            fi
            ;;
        off|false|no|0)
            SHOW_CLOCK="false"
            log_info "Clock display disabled"
            ;;
        *)
            log_error "Invalid value: $enabled (use on/off)"
            return 1
            ;;
    esac

    save_config
}

